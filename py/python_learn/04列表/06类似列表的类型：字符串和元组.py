#4.6.2元组数据类型
#元组：元组数据类型和列表数据类型几乎一样；主要有两个不同：1、元组数据输入用括号()包裹；2、元组得值像字符串一样，不能被修改、添加或删除
eggs = ('h','l',3)
# eggs[1] = 99 修改元组会报类型错误

#如果一个元组只有一个值，你可以在括号内该值后面加一个逗号，表明这是一个元组；否则解析器会认为，你只是在普通括号内输入一个值
C = ('hell',)
D = ('hell')
print(type(C))

#元组得意义：你可以用元组告诉读代码得人，你不打算修改这个序列中得值；如果一个序列中得值永远不需改变就用元组，使用元组可能会比使用列表更快

