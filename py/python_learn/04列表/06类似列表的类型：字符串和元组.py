#4.6.1 可变和不可变数据类型
#列表和字符串的不同：列表时可变的数据类型，列表的值可以添加、删除、改变。字符串是不可改变的、尝试对字符串重新赋值，将报TypeError错误
name = 'sophia'
print(name[1])
# name[1] = "b"

#如何修改一个字符串
#使用切片从字符串中复制一部分出来，用+连接新的字符，变成新的字符串
name = 'sophia is a girl'
newName = name[0:8] + ' the ' + name[11:16]
print(newName)

#如何修改一个列表中的值
#单纯给列表A的变量名重新赋值一个列表B是不会改变列表A中的任何值得。
#修改一个列表中值得方法：列用列表得下标使用del把列表中得值给删除、然后用appen方法把新的值给添加进去

